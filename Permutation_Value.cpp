#include <bits/stdc++.h>
using namespace std;
int main()
{
    int tt;
    cin >> tt;
    while (tt--)
    {
        int n;
        cin >> n;
        cout << "1"
             << " ";
        for (int i = n; i > 1; i--)
        {
            cout << i << " ";
        }
        cout << endl;
    }
}